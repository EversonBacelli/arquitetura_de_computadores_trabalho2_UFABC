
def definirSeEmaior(destino, operando1, operando2):
    destino = 1 if operando1 > operando2 else 0
    return destino

def definirSeEmenor(destino, operando1, operando2):
    destino = 1 if operando1 < operando2 else 0
    return destino

def definirSeEigual(destino, operando1, operando2):
    destino = 1 if operando1 == operando2 else 0
    return destino

