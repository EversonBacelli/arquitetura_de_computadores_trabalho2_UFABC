
def definirSeEmaior(operando1, operando2):
    resposta = 1 if operando1 > operando2 else 0
    return resposta

def definirSeEmenor(operando1, operando2):
    resposta = 1 if operando1 < operando2 else 0
    return resposta

def definirSeEigual(operando1, operando2):
    resposta = 1 if operando1 == operando2 else 0
    return resposta

